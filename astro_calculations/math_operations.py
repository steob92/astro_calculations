def add(a, b):
    """Add two numbers together

    Args:
        a : input value, float or int
        b : input value, float or int
    
    Returns:
        a + b
    """
    return a + b

def sub(a, b):
    """Subtract two numbers together

    Args:
        a : input value, float or int
        b : input value, float or int
    
    Returns:
        a - b
    """
    return a - b

def mult(a, b):
    """Multiply two numbers together

    Args:
        a : input value, float or int
        b : input value, float or int
    
    Returns:
        a * b
    """
    return a * b

def div(a, b):
    """Divide one number by another

    Args:
        a : input value, float or int
        b : input value, float or int
    
    Returns:
        a / b
    """
    return a / b
